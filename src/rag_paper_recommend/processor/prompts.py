"""LLM に渡すプロンプト定義。

プロンプトを一箇所に集めることで、品質改善時の変更コストを最小化する。

Stage 1 (抽出): EXTRACTION_SYSTEM / EXTRACTION_PROMPT
Stage 2 (合成): SYNTHESIS_SYSTEM / WEEKLY_SYNTHESIS_PROMPT / MONTHLY_SYNTHESIS_PROMPT
Stage 3 (日次トレンド): TREND_ANALYSIS_SYSTEM / DAILY_TREND_PROMPT
Stage 4 (ブートストラップ): BOOTSTRAP_CHUNK_SYSTEM / BOOTSTRAP_CHUNK_PROMPT / BOOTSTRAP_FINAL_PROMPT
"""

# ------------------------------------------------------------------ #
# Stage 1: 個別論文の構造化抽出                                         #
# ------------------------------------------------------------------ #

EXTRACTION_SYSTEM = """\
あなたは研究論文を分析する専門家です。
論文のタイトルとアブストラクトから研究の構造を正確に抽出し、
指定された JSON 形式のみを返してください。余分なテキストや説明は不要です。\
"""

EXTRACTION_PROMPT = """\
以下の論文を分析し、JSON 形式で返してください。

タイトル: {title}
著者: {authors}
アブストラクト:
{abstract}

以下のキーを持つ JSON オブジェクトを返してください（値はすべて日本語）:
{{
  "problem": "この研究が解決しようとしている問題や課題（2〜3文）",
  "method": "提案している手法やアプローチ（2〜3文）",
  "claims": "主要な実験結果や主張（2〜3文）",
  "limitations": "研究の限界や制約（1〜2文）",
  "open_questions": "著者が示す未解決問題や今後の課題（1〜2文）"
}}\
"""

# ------------------------------------------------------------------ #
# Stage 2: 横断合成（週次・月次）                                        #
# ------------------------------------------------------------------ #

SYNTHESIS_SYSTEM = """\
あなたは研究トレンドを分析するエキスパートアナリストです。
複数の論文データをもとに、研究分野の動向・課題・方向性を鋭く分析してください。
研究者が「この分野で何が起きているか」「次に何を研究すべきか」を
判断できるような洞察を提供してください。\
"""

WEEKLY_SYNTHESIS_PROMPT = """\
以下は「{topic}」に関する {count} 件の論文データです（{start} 〜 {end}）。

{papers_data}

---

これらの論文をもとに、以下の構成で **Markdown 形式** の週次研究動向レポートを書いてください。

## 今週の概況
（全体的な傾向を 2〜3 文で。どんなアプローチが多かったか、研究の熱量はどうか）

## 新しいアプローチ・手法
（今週初めて、または注目すべき形で登場したアイデア・手法を箇条書きで）

## 繰り返し言及される未解決問題
（複数の論文で共通して指摘されている課題を箇条書きで。この分野が今どこで詰まっているか）

## 注目の研究方向性
（今後の研究が向かっていると思われる方向性を箇条書きで）

## 今週の重要論文（最大 3 件）
（特に重要と思われる論文のタイトルと、その理由を 1〜2 文で）\
"""

MONTHLY_SYNTHESIS_PROMPT = """\
以下は「{topic}」に関する {count} 件の論文データです（{start} 〜 {end}）。

{papers_data}

---

これらの論文をもとに、以下の構成で **Markdown 形式** の月次研究トレンドレポートを書いてください。

## 今月のトレンド概要
（この 1 ヶ月の全体像を 3〜4 文で）

## 急速に発展している領域
（今月特に活発だったテーマ・手法・アプローチ）

## 継続して取り組まれている課題
（先月以前からある未解決問題で、今月も研究が続いているもの）

## 新たに浮上した問題・課題
（今月になって多く言及されるようになった問題や議論）

## 研究の空白・チャンス領域
（あまり取り組まれていないが重要と思われる問題や方向性）

## 今後の研究方向性
（この分野で研究する上で有望な方向性を具体的に）

## 今月の重要論文（最大 5 件）
（特に重要と思われる論文のタイトルと、その理由を 1〜2 文で）\
"""

# ------------------------------------------------------------------ #
# Stage 3: 日次トレンド分析（今日 vs 過去 7 日）                         #
# ------------------------------------------------------------------ #

TREND_ANALYSIS_SYSTEM = """\
あなたは研究トレンドを毎日追跡するアナリストです。
「今日の論文」と「過去 7 日間の論文」を比較して、
研究者が毎朝メールで読む想定で、簡潔かつ洞察に富んだ分析を提供してください。
情報がない場合は「特になし」と正直に書いてください。\
"""

DAILY_TREND_PROMPT = """\
# 今日の論文（{today_date}、{today_count} 件）
{today_papers_data}

# 過去 7 日間の論文（{past_count} 件、比較用）
{past_papers_data}

---

上記をもとに、以下の構成で **Markdown 形式** の日次トレンド分析を書いてください。

## 今日の新しいアプローチ・手法
（過去 7 日間に見られなかった、または今日初めて登場したアイデア・手法。なければ「特になし」と書いてください）

## 直近 7 日間のトレンド・繰り返し言及される課題
（複数の論文に共通するテーマや未解決問題を箇条書きで）

## 今日の注目論文
（今日の論文の中で、新規性・インパクトの観点で特に注目すべきもの。なければ「特になし」と書いてください）\
"""

# ------------------------------------------------------------------ #
# Stage 4: ブートストラップ分析（初回・大量論文の2段階処理）               #
# ------------------------------------------------------------------ #

BOOTSTRAP_CHUNK_SYSTEM = """\
あなたは研究論文を分析する専門家です。
与えられた論文群から、研究トレンド・手法・課題を簡潔に要約してください。
この要約は後で複数まとめて最終分析に使われます。\
"""

BOOTSTRAP_CHUNK_PROMPT = """\
以下は「{topic}」に関する論文 {count} 件です（{start} 〜 {end} の一部）。

{papers_data}

---

この論文群を読んで、以下を箇条書きで簡潔にまとめてください。

### 登場した主要な手法・アプローチ
### 繰り返し言及されている課題・問題
### 注目すべき論文（最大 3 件、タイトルと理由を 1 文）\
"""

BOOTSTRAP_FINAL_SYSTEM = """\
あなたは研究分野の動向を俯瞰するエキスパートアナリストです。
複数の中間サマリーをもとに、研究者が「この分野の現状と今後」を理解できる
包括的なレポートを生成してください。研究アイデアや空白領域の指摘も含めてください。\
"""

BOOTSTRAP_FINAL_PROMPT = """\
以下は「{topic}」に関する過去 {days} 日間（{paper_count} 件）の論文を
{chunk_count} グループに分けて要約した中間サマリーです。

{summaries}

---

これらをもとに、以下の構成で **Markdown 形式** の包括的なフィールド概観レポートを作成してください。

## 分野の現状概要
（この {days} 日間で何が起きていたか、全体像を 4〜5 文で）

## 主要なトレンド・活発な研究領域
（特に活発に研究されているテーマ・手法・アプローチ）

## 分野が直面している主要課題
（複数の研究が共通して取り組んでいる未解決問題）

## 有望な研究方向性
（今後の研究で成果が期待できる方向性を具体的に）

## 研究の空白・チャンス領域
（まだ十分に取り組まれていないが重要と思われるテーマや組み合わせ）

## 具体的な研究アイデア
（既存研究のギャップや組み合わせから生まれる、実行可能な研究アイデアを 3〜5 個）

## 特に重要な論文（最大 5 件）
（この期間で特に影響力・新規性が高いと思われる論文とその理由）\
"""
